"""Детектор почтовых адресов РФ (ADDRESS) — по ключевым словам улиц.

Якорится на тип улицы (ул./проспект/переулок/...), затем захватывает название
до запятой или конца строки. Ключевое слово держит точность высокой.
"""
from __future__ import annotations

from typing import List

from datashield.detectors.base import KeywordContextDetector, RegexDetector

__all__ = ["build"]

_STREET = (
    r"(?i:улиц[аеыу]|ул\.|проспект\w*|просп\.|пр-?кт|переул\w*|пер\.|бульвар\w*"
    r"|бул\.|шоссе|ш\.|набережн\w*|наб\.|площад\w*|пл\.|проезд)"
)


def build() -> List[object]:
    return [
        # Улица + название. Лукэхед требует заглавную/цифру/кавычку после
        # слова-улицы, чтобы нарицательное употребление («шоссе было пустым»,
        # «на площади собрались») не маскировалось как адрес.
        RegexDetector(
            "address_ru", "ADDRESS",
            _STREET + r"\s+(?=[А-ЯЁA-Z0-9\"«№])[^\n,;]{2,40}", 0.78,
        ),
        # 6-значный почтовый индекс — только при слове «индекс» рядом.
        KeywordContextDetector(
            "postal_code_ru", "POSTAL_CODE", r"(?<!\d)\d{6}(?!\d)",
            0.3, 0.85, r"индекс",
        ),
    ]
